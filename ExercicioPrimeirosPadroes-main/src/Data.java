public class Data {
    
    public String dia;
    public String mes;
    public String ano;
    
    

    public Data(String dia, String mes, String ano) {
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
    }

    public String getDia() {
        return dia;
    }



    public String getMes() {
        return mes;
    }



    public String getAno() {
        return ano;
    }
    
    

}
